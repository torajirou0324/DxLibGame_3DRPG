//-----------------------------------------------------------------------------
// @brief  更新処理.
//-----------------------------------------------------------------------------
TAG_SCENE EndScene::Update()
{
	if (Input::IsPress(ENTER))
	{
		return TAG_SCENE::TAG_TITLE;
	}
	Player::Update();
	return TAG_SCENE::TAG_NONE;
}

